@pytest.mark.asyncio
async def test_audit_events_sorted_by_created_at_desc(api_client):
    """GET /audit/events should return events sorted by created_at descending."""
    # Arrange — create three sources to generate ordered audit events
    tag = uuid.uuid4().hex[:6]
    names = [f"sort-audit-{i}-{tag}" for i in range(3)]
    for name in names:
        await api_client.post(
            "/sources/propose",
            json={"name": name, "kind": "web"},
            headers={"Authorization": "Bearer test-secret"},
        )

    # Act
    resp = await api_client.get(
        "/audit/events",
        headers={"Authorization": "Bearer test-secret"},
    )

    # Assert — newest first
    assert resp.status_code == 200
    data = resp.json()
    events = data["items"]
    assert len(events) >= 3
    for i in range(len(events) - 1):
        assert events[i]["created_at"] >= events[i + 1]["created_at"]


