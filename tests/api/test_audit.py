# ---------------------------------------------------------------------------


@pytest.mark.asyncio
async def test_audit_events_do_not_expose_raw_payload_fields(api_client):
    """Audit event response should not include before_state/after_state raw JSONB."""
    # Arrange
    await api_client.post(
        "/sources/propose",
        json={"name": f"no-raw-{uuid.uuid4().hex[:6]}", "kind": "web"},
        headers={"Authorization": "Bearer test-secret"},
    )

    # Act
    resp = await api_client.get(
        "/audit/events",
        headers={"Authorization": "Bearer test-secret"},
    )

    # Assert — the response should contain summary fields, not raw state
    assert resp.status_code == 200
    data = resp.json()
    for event in data["items"]:
        # Raw before_state/after_state JSONB must not be present
        assert "before_state" not in event, "before_state must not be exposed"
        assert "after_state" not in event, "after_state must not be exposed"
        # Summary fields should be present instead
        assert "before_summary" in event
        assert "after_summary" in event
